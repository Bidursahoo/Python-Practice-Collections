def lb_to_kg(weight):
    return weight *0.45

def square(num):
    return num**2

def kg_to_lb(weight):
    return weight/0.45

class Square:
    def area(side):
        return side * side
    def peremeter(side):
        return 4*side

class Rectangle:
    def area(length , breath):
        return length*breath
    def peremeter(length , breath):
        return 2*(length + breath)